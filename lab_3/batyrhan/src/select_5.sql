USE Дипломное_проектирование

SELECT DISTINCT [Категории_типов_проектов].[наименование] AS [Категория]
FROM dbo.[Категории_типов_проектов]
JOIN dbo.[Проект]
  ON [Проект].[код_типа_категории_проекта] = [Категории_типов_проектов].[код_типа_категории_проекта]
JOIN dbo.[Студенты]
  ON [Студенты].[код_студента] = [Проект].[код_студента]
JOIN dbo.[График_защиты]
  ON [График_защиты].[код_студента] = [Студенты].[код_студента]
WHERE DAY([График_защиты].[дата]) = 1;
